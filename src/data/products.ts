export function getProductsByCategory(category: string): Product[] {
  return products.filter((p) => p.category.toLowerCase().replace(/\s+/g, "-") === category.toLowerCase());
}

export function formatPrice(price: number): string {
  return new Intl.NumberFormat("en-IN", {
    style: "currency",
    currency: "INR",
    minimumFractionDigits: 0,
  }).format(price);
}

// Fallback image based on category
export function getCategoryFallback(category: string): string {
  const colors: Record<string, { bg: string; text: string }> = {
    "AI Tools": { bg: "#ff6b35", text: "AI" },
    "Writing Tools": { bg: "#00d4ff", text: "Writing" },
    "Video Editing": { bg: "#9333ea", text: "Video" },
    "Indian OTT": { bg: "#ef4444", text: "OTT" },
    "International OTT": { bg: "#3b82f6", text: "OTT" },
    "Cloud Services": { bg: "#10b981", text: "Cloud" },
    "Software": { bg: "#6366f1", text: "Software" },
    "Lead Generation": { bg: "#f59e0b", text: "Leads" },
  };
  const config = colors[category] || { bg: "#ff6b35", text: category.split(" ")[0] };
  return `data:image/svg+xml,${encodeURIComponent(`<svg xmlns="http://www.w3.org/2000/svg" width="400" height="300"><rect fill="${config.bg}" width="400" height="300"/><text x="50%" y="50%" dominant-baseline="middle" text-anchor="middle" fill="white" font-size="48" font-family="system-ui">${config.text}</text></svg>`)}`;
}
